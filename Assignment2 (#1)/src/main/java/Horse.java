public class Horse {

    private String name;
    private String color;
    private int year;


    public void setName(String HorseName) {
        this.name = HorseName;
    }

    public void setColor(String HorseColor) {
        this.color = HorseColor;
    }

    public void setYear(int BirthYear) {
        this.year = BirthYear;
    }

    public String getName() {
        return this.name;
    }

    public String getColor() {
        return this.color;
    }

    public int getYear() {
        return this.year;
    }

}





